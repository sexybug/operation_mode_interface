
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>

void format_data(char *out, size_t *outlen, const char *in, size_t inlen, int word_size, int words_per_line)
{
    size_t i, j = 0;
    for (i = 0; i < inlen; i += 1)
    {
        out[j++] = in[i];
        if ((i + 1) % (words_per_line * (word_size * 2 + 4)) == 0)
        {
            out[j++] = '\n';
        }
    }
    out[j] = '\0';
    *outlen = j;
}

int main()
{
    const char *in = "0x1A, 0xB2, 0x1D, 0x83, 0x55, 0xCF, 0xA1, 0x7F, 0x8E, 0x61, 0x19, 0x48, 0x31, 0xE8, 0x1A, 0x8F, 0x22, 0xBE, 0xC8, 0xC7, 0x28, 0xFE, 0xFB, 0x74, 0x7E, 0xD0, 0x35, 0xEB, 0x50, 0x82, 0xAA, 0x2B";
    char out[1000];
    size_t outlen = 0;
    format_data(out, &outlen, in, strlen(in), 1, 4);
    printf("%s\n", out);

    const char *in2 = "0x1A, 0xB2, 0x1D, 0x83, 0x55, 0xCF, 0xA1, 0x7F, 0x8E, 0x61, 0x19, 0x48, 0x31, 0xE8, 0x1A, 0x8F, 0x22, 0xBE, 0xC8, 0xC7, 0x28, 0xFE, 0xFB, 0x74, 0x7E, 0xD0, 0x35, 0xEB, 0x50, 0x82, 0xAA, 0x2B, 0x00";
    format_data(out, &outlen, in2, strlen(in2), 1, 4);
    printf("%s\n", out);

    const char *in3 = "0x1AB21D83, 0x55CFA17F, 0x8E611948, 0x31E81A8F, 0x22BEC8C7, 0x28FEFB74, 0x7ED035EB, 0x5082AA2B";
    format_data(out, &outlen, in3, strlen(in3), 4, 4);
    printf("%s\n", out);

    return 0;
}